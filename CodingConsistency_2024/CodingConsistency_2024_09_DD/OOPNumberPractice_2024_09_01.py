class NumbersOnumbers:
    def __init__(self,num1,num2) -> None:
        self.num1 = num1
        self.num2 = num2
    
    def additioningSum(self):
        print(self.num1 + self.num2)
    def subtractingSum(self):
        print(self.num1 - self.num2)
    def multiplyingSum(self):
        print(self.num1 * self.num2)
    def dividingSum(self):
        print(self.num1 / self.num2)
    
obj = NumbersOnumbers(5,10)

obj.additioningSum()
obj.subtractingSum()
obj.multiplyingSum()
obj.dividingSum()

